package it.rt.corso.beans;

public class Mansione implements Bean{
	
	private int id;
	private String mansione;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMansione() {
		return mansione;
	}
	public void setMansione(String mansione) {
		this.mansione = mansione;
	}
	
	
	
}
